package com.chrtc.excelTest.excelTest.domain;

public class InputValue {
    private String  fieldName;
    private String  fieldEng;
    private String  fieldRemark;
    private String  fieldLength;
    private String  fieldHeadName;
    private String  fieldOption;

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public String getFieldRemark() {
        return fieldRemark;
    }

    public String getFieldEng() {
        return fieldEng;
    }

    public void setFieldEng(String fieldEng) {
        this.fieldEng = fieldEng;
    }

    public void setFieldRemark(String fieldRemark) {
        this.fieldRemark = fieldRemark;
    }

    public String getFieldLength() {
        return fieldLength;
    }

    public void setFieldLength(String fieldLength) {
        this.fieldLength = fieldLength;
    }

    public String getFieldHeadName() {
        return fieldHeadName;
    }

    public void setFieldHeadName(String fieldHeadName) {
        this.fieldHeadName = fieldHeadName;
    }

    public String getFieldOption() {
        return fieldOption;
    }

    public void setFieldOption(String fieldOption) {
        this.fieldOption = fieldOption;
    }
}
